x = 5
y = 2

# Addition
puts x+y 
# outputs 7

# Subtraction 
puts x-y 
# outputs 3

# Multiplication
puts x*y
# outputs 10

# Division
puts x/y
# outputs 2

# Modulus
puts x%y
# outputs 1

# Exponent
print x**y
# this raises 2 to the power of 5 and outputs 25

# Shorhand Assignment
x += y  # x=x+y
x -= y  # x=x-y
x *= y  # x=x*y
x /= y  # x=x/y
x %= y  # x=x%y
x **= y  # x=x**y